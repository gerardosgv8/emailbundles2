export function downloadDesignRules(state: DesignRulesState): void {
  const blob = new Blob([exportDesignRules(state)], { type: 'text/markdown;charset=utf-8' });
  const a = document.createElement('a');
  a.href = URL.createObjectURL(blob);
  a.download = 'DESIGN_RULES.md';
  a.click();
  URL.revokeObjectURL(a.href);
}
