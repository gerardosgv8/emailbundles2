import type { DesignRulesState } from './types';
import { CHECKLIST_ITEMS } from './defaults';

function escMd(s: string | undefined | null): string {
  return String(s ?? '').replace(/\|/g, '\\|');
}

function val(s: string): string {
  return s ? s : '';
}

